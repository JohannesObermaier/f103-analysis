		__asm__ volatile("start1: ldr r4, =0x48000c10"); //GPIOD input register
		__asm__ volatile("ldr r5, =0x48000c00"); //GPIOD MODER
		__asm__ volatile("ldr r6, =0x48000010"); //GPIOA MODER
		__asm__ volatile("ldr r7, =0x48000c18"); //GPIOD BSSR
		__asm__ volatile("ldr r8, =0x48001018"); //GPIOE BSSR
		__asm__ volatile("mov r0, #3"); //set high PD0+PD1
